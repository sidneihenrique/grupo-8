public class Campeao {
    private String nome;
    private String skin;

    public Campeao(String nome, String skin){
        this.nome = nome;
        this.skin = skin;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSkin() {
        return skin;
    }

    public void setSkin(String skin) {
        this.skin = skin;
    }
}
